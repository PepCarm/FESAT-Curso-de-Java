    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String marca = jTextField1.getText();
        String modelo = jTextField2.getText();
        float precio = Float.parseFloat(jTextField3.getText());

        Vehiculo objetoVehiculo = new Vehiculo(marca, modelo, precio);

        arrayVehiculos.add(objetoVehiculo);
        System.out.println(arrayVehiculos);
        DefaultTableModel datosTabla = (DefaultTableModel) jTable1.getModel();
        datosTabla.setNumRows(0);

        for (int i = 0; i < arrayVehiculos.size(); i++) {
            Object[] filaTabla = {arrayVehiculos.get(i).getMarca(),
                arrayVehiculos.get(i).getModelo(),
                arrayVehiculos.get(i).getPrecio()};
            datosTabla.addRow(filaTabla);

        }
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField3.setText(null);
        jButton2.setEnabled(true);
                

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        float precio;
        int posicion=0;
        precio=arrayVehiculos.get(0).getPrecio();
        for (int i = 0; i < arrayVehiculos.size(); i++) {
            if (arrayVehiculos.get(i).getPrecio()<precio) {
                precio=arrayVehiculos.get(i).getPrecio();
                posicion=i;
            }
        }
        JOptionPane.showMessageDialog(this, "EL AUTOMÓVIL DE MENOR PRECIO ES: "+arrayVehiculos.get(posicion).mostrarDatos());
    }//GEN-LAST:event_jButton2ActionPerformed
    

